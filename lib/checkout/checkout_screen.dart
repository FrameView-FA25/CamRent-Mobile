            ],
          ),
          child: ElevatedButton.icon(
            onPressed: _isSubmitting ? null : _handleCheckout,
            icon: _isSubmitting
                ? const SizedBox(
                    width: 20,
                    height: 20,
                    child: CircularProgressIndicator(
                      strokeWidth: 2,
                      color: Colors.white,
                    ),
                  )
                : const Icon(Icons.payment),
            label: Text(
              _isSubmitting ? 'Đang xử lý...' : 'Thanh toán',
              style: const TextStyle(
